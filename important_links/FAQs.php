    <script>
        // Simple JavaScript for FAQ functionality
        document.addEventListener('DOMContentLoaded', function() {
            const faqQuestions = document.querySelectorAll('.faq-question');
            
            faqQuestions.forEach(question => {
                question.addEventListener('click', () => {
                    const item = question.parentNode;
                    item.classList.toggle('active');
                    
                    // Close other open items
                    faqQuestions.forEach(otherQuestion => {
                        if (otherQuestion !== question && otherQuestion.parentNode.classList.contains('active')) {
                            otherQuestion.parentNode.classList.remove('active');
                        }
                    });
                });
            });
            
            // Simple search functionality
            const searchInput = document.querySelector('.search-box input');
            const searchButton = document.querySelector('.search-box button');
            
            searchButton.addEventListener('click', performSearch);
            searchInput.addEventListener('keypress', function(e) {
                if (e.key === 'Enter') {
                    performSearch();
                }
            });
            
            function performSearch() {
                const searchTerm = searchInput.value.toLowerCase();
                if (searchTerm.trim() === '') return;
                
                const faqItems = document.querySelectorAll('.faq-item');
                let foundResults = false;
                
                faqItems.forEach(item => {
                    const question = item.querySelector('.faq-question').textContent.toLowerCase();
                    const answer = item.querySelector('.faq-answer').textContent.toLowerCase();
                    
                    if (question.includes(searchTerm) || answer.includes(searchTerm)) {
                        item.style.display = '';
                        foundResults = true;
                        
                        // Open the matching item
                        item.classList.add('active');
                        
                        // Highlight the matching text (simple approach)
                        item.scrollIntoView({ behavior: 'smooth', block: 'center' });
                    } else {
                        item.style.display = 'none';
                    }
                });
                
                if (!foundResults) {
                    alert('No FAQs found matching your search. Try different keywords.');
                    // Reset display if no results
                    faqItems.forEach(item => {
                        item.style.display = '';
                    });
                }
            }
        });
    </script>
