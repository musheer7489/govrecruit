        </div>

        <div class="rules-section">
            <h2>Appointment & Probation</h2>

            <h3>Offer of Appointment</h3>
            <p>Successful candidates will receive:</p>
            <ul>
                <li>Formal appointment letter specifying the terms of employment</li>
                <li>Joining instructions with reporting date and location</li>
                <li>List of documents to bring for verification</li>
            </ul>

            <h3>Probation Period</h3>
            <p>All new appointments are subject to a probation period:</p>
            <ul>
                <li>2 years for Group A and B positions</li>
                <li>1 year for Group C positions</li>
                <li>Probation may be extended in exceptional circumstances</li>
                <li>Confirmation is subject to satisfactory performance during probation</li>
            </ul>
        </div>

        <div class="rules-section">
            <h2>Code of Conduct for Applicants</h2>

            <p>All applicants must adhere to the following code of conduct:</p>
            <ul>
                <li>Provide accurate and complete information in the application</li>
                <li>Do not submit forged or fraudulent documents</li>
                <li>Do not attempt to influence the selection process through improper means</li>
                <li>Maintain decorum during examinations and interviews</li>
                <li>Do not engage in any malpractice during selection tests</li>
            </ul>

            <div class="note-box">
                <p><strong>Note:</strong> Violation of the code of conduct may lead to disqualification from the current recruitment process and debarment from future recruitments.</p>
