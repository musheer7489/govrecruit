        <div class="rules-section">
            <h2>General Recruitment Principles</h2>

            <p>The recruitment process for government positions is governed by the following principles:</p>

            <ul>
                <li><strong>Merit-based selection:</strong> All appointments shall be made on the basis of merit through fair and open competition</li>
                <li><strong>Equal opportunity:</strong> No applicant shall be discriminated against on grounds of gender, religion, race, or ethnicity</li>
                <li><strong>Transparency:</strong> The recruitment process shall be conducted in a transparent manner</li>
                <li><strong>Accountability:</strong> All decisions shall be properly documented and justifiable</li>
            </ul>

            <div class="note-box">
                <p>These rules apply to all permanent government positions unless specifically exempted by law.</p>
            </div>
        </div>

        <div class="rules-section">
            <h2>Eligibility Criteria</h2>

            <h3>Basic Qualifications</h3>
            <p>All applicants must meet the following minimum requirements:</p>

            <table class="rules-table">
                <thead>
                    <tr>
                        <th>Requirement</th>
                        <th>Description</th>
                    </tr>
                </thead>
                <tbody>
                    <tr>
                        <td>Citizenship</td>
                        <td>Must be a citizen or have permanent residency with work authorization</td>
                    </tr>
                    <tr>
                        <td>Age Limit</td>
                        <td>Minimum 18 years (some positions may have upper age limits)</td>
                    </tr>
                    <tr>
                        <td>Educational Qualifications</td>
                        <td>As specified in the job announcement (verified through original documents)</td>
                    </tr>
                    <tr>
                        <td>Character</td>
                        <td>Must possess good moral character and no criminal record</td>
                    </tr>
                </tbody>
            </table>

            <h3>Relaxation of Rules</h3>
            <p>In exceptional cases, the following relaxations may apply:</p>
            <ul>
                <li>Age limit may be relaxed for reserved categories as per government policy</li>
                <li>Educational qualifications may be relaxed for internal candidates with exceptional experience</li>
                <li>Physical standards may be relaxed for persons with disabilities</li>
            </ul>
        </div>

        <div class="rules-section">
            <h2>Selection Process</h2>

            <h3>Standard Process</h3>
            <p>The typical selection process includes the following stages:</p>
            <ol>
                <li>Advertisement of vacancy</li>
                <li>Receipt of applications</li>
                <li>Screening of applications</li>
                <li>Written examination (where applicable)</li>
                <li>Skill tests/practical tests (for technical positions)</li>
                <li>Interview</li>
                <li>Document verification</li>
                <li>Medical examination (for certain positions)</li>
                <li>Final selection and appointment</li>
            </ol>

            <h3>Weightage of Components</h3>
            <p>The final selection shall be based on the following weightage unless otherwise specified:</p>
            <table class="rules-table">
                <thead>
                    <tr>
                        <th>Component</th>
                        <th>Weightage</th>
                        <th>Remarks</th>
                    </tr>
                </thead>
                <tbody>
                    <tr>
                        <td>Written Examination</td>
                        <td>60%</td>
                        <td>Minimum qualifying marks: 50% (45% for reserved categories)</td>
                    </tr>
                    <tr>
                        <td>Interview</td>
                        <td>30%</td>
                        <td>Only for candidates who qualify written exam</td>
                    </tr>
                    <tr>
                        <td>Experience/Additional Qualifications</td>
                        <td>10%</td>
                        <td>As per advertised criteria</td>
                    </tr>
                </tbody>
            </table>
        </div>

